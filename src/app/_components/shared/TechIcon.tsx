interface TechIconProps {
  name: string;
  className?: string;
  size?: number;
  showColor?: boolean;
}

export const TechIcon: FC<TechIconProps> = ({
  name,
  className = "",
  size = 24,
  showColor = true,
}) => {
  const normalized = name.toLowerCase().trim();
  const IconComponent = iconMap[normalized] ?? DefaultIcon;
  const color = showColor ? getIconColor(name) : "currentColor";

  return (
    <IconComponent
      className={className}
      width={size}
      height={size}
      style={{ color }}
    />
  );
};

// Helper to get icon by FontAwesome name (for backwards compatibility)
export const getTechIconByFaName = (faName: string): FC<IconProps> => {
  const normalized = faName.toLowerCase().replace("fa", "");
  return iconMap[normalized] ?? iconMap[`fa${normalized}`] ?? DefaultIcon;
};

export default TechIcon;

